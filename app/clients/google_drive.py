"""
Google Drive API client.

Thin wrapper around Drive API v3 for the folder-picker feature:
  - list_folders()  — list all folders the user has access to (optionally under a parent)
  - search_folders() — name-based search
  - get_folder_metadata() — fetch a single folder's metadata
  - list_files_in_folder() — list files inside a selected folder (used later by sync)
  - upload_as_google_doc() — upload a .docx and convert it to a Google Doc in Drive

Auth model:
The caller passes a `token_data` dict (same shape as the one stored on
UserEmailConnection.token_data — {token, refresh_token, scopes, expiry}).
We reuse the refresh helper from google_docs.py so token refresh logic
lives in one place.

Scopes required:
  - drive.readonly  — read/download files (template fetch, indexing)
  - drive.file      — create/upload files generated by Zippy (MOM, NDA, drafts)
Both are requested as part of PERSONAL_OAUTH_SCOPES during the Gmail OAuth flow.
"""
from __future__ import annotations

import json
import logging
from typing import Any, Optional

import httpx

from app.clients.google_docs import DRIVE_SCOPE, _refresh_token_if_needed

logger = logging.getLogger(__name__)

DRIVE_API_BASE = "https://www.googleapis.com/drive/v3"
DRIVE_UPLOAD_BASE = "https://www.googleapis.com/upload/drive/v3"
FOLDER_MIME = "application/vnd.google-apps.folder"
SHORTCUT_MIME = "application/vnd.google-apps.shortcut"
DRIVE_FILE_SCOPE = "https://www.googleapis.com/auth/drive.file"
GDOC_MIME = "application/vnd.google-apps.document"
DOCX_MIME = "application/vnd.openxmlformats-officedocument.wordprocessingml.document"


def _has_drive_scope(token_data: dict) -> bool:
    scopes = token_data.get("scopes", [])
    if isinstance(scopes, list):
        return any(DRIVE_SCOPE in scope for scope in scopes)
    return DRIVE_SCOPE in str(scopes)


def _has_drive_write_scope(token_data: dict) -> bool:
    """True if the token has drive.file or full drive scope (allows creating files)."""
    scopes = token_data.get("scopes", [])
    scope_str = " ".join(scopes) if isinstance(scopes, list) else str(scopes)
    return DRIVE_FILE_SCOPE in scope_str or "auth/drive " in scope_str or scope_str.endswith("auth/drive")


async def _ensure_token(
    token_data: dict,
    client_id: str,
    client_secret: str,
) -> tuple[str, dict]:
    """Refresh the token if needed. Returns (access_token, updated_token_data)."""
    if not _has_drive_scope(token_data):
        raise PermissionError(
            "Token does not include drive.readonly scope — user must reconnect Gmail."
        )
    updated = await _refresh_token_if_needed(token_data, client_id, client_secret)
    access_token = updated.get("token")
    if not access_token:
        raise ValueError("Failed to obtain a valid access token for Drive API")
    return access_token, updated


async def upload_as_google_doc(
    *,
    filename: str,
    docx_bytes: bytes,
    token_data: dict,
    client_id: str,
    client_secret: str,
    parent_folder_id: Optional[str] = None,
) -> tuple[str, str]:
    """Upload a .docx file to Drive and convert it to a Google Doc.

    Uses a multipart upload with ``mimeType = application/vnd.google-apps.document``
    so Drive converts the Word content automatically.

    Returns ``(drive_file_id, webViewLink)`` on success.
    Raises ``PermissionError`` if the token lacks drive.file scope (user needs
    to reconnect Gmail so the new scope is granted).
    """
    if not _has_drive_write_scope(token_data):
        raise PermissionError(
            "Token does not include drive.file scope. "
            "The user must reconnect Google in Settings to grant Zippy "
            "permission to create files in Drive."
        )

    access_token, _ = await _ensure_token(token_data, client_id, client_secret)

    metadata: dict[str, Any] = {
        "name": filename,
        "mimeType": GDOC_MIME,  # tells Drive to convert the uploaded .docx
    }
    if parent_folder_id:
        metadata["parents"] = [parent_folder_id]

    boundary = "zippy_mp_boundary_x9k2"
    meta_bytes = json.dumps(metadata).encode()

    # Multipart body: metadata part + file bytes part
    body = (
        f"--{boundary}\r\n"
        f"Content-Type: application/json; charset=UTF-8\r\n\r\n"
    ).encode()
    body += meta_bytes
    body += f"\r\n--{boundary}\r\n".encode()
    body += f"Content-Type: {DOCX_MIME}\r\n\r\n".encode()
    body += docx_bytes
    body += f"\r\n--{boundary}--".encode()

    async with httpx.AsyncClient(timeout=60) as http:
        resp = await http.post(
            f"{DRIVE_UPLOAD_BASE}/files",
            headers={
                "Authorization": f"Bearer {access_token}",
                "Content-Type": f"multipart/related; boundary={boundary}",
            },
            params={
                "uploadType": "multipart",
                "fields": "id,webViewLink",
                "supportsAllDrives": "true",
            },
            content=body,
        )

    if resp.status_code not in (200, 201):
        raise RuntimeError(
            f"Drive upload failed (HTTP {resp.status_code}): {resp.text[:400]}"
        )

    data = resp.json()
    file_id = data.get("id", "")
    web_view_link = data.get("webViewLink", "")
    logger.info("Zippy doc uploaded to Drive: file_id=%s url=%s", file_id, web_view_link)
    return file_id, web_view_link


